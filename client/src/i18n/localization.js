export function normalizeLanguage(language) {
  return supportedLanguageIds.has(language) ? language : defaultLanguage;
}

export function t(key, language = "en", variables = {}) {
  const lang = normalizeLanguage(language);
  const template = ui[lang]?.[key] ?? ui.en[key] ?? key;
  return Object.entries(variables).reduce(
    (result, [name, value]) => result.replaceAll(`{${name}}`, String(value)),
    template
  );
}

export function content(group, id, field, fallback, language = "en") {
  if (normalizeLanguage(language) !== "zh") return fallback;
  return translateEmbeddedTerms(zhContent[group]?.[id]?.[field] ?? fallback, language);
}

export function nameFor(group, value, language = "en") {
  const lang = normalizeLanguage(language);
  return names[lang]?.[group]?.[value] ?? value;
}

export function itemName(value, language = "en") {
  return nameFor("items", value, language);
}

export function areaName(value, language = "en") {
  return nameFor("areas", value, language);
}

export function elementName(value, language = "en") {
  return nameFor("elements", value, language);
}

export function abilityName(value, language = "en") {
  return nameFor("abilities", value, language);
}

export function symbolName(value, language = "en") {
  return nameFor("symbols", value, language);
}

export function formatItems(items = {}, language = "en", prefix = "") {
  return Object.entries(items)
    .map(([name, amount]) => `${prefix}${amount} ${itemName(name, language)}`)
    .join(language === "zh" ? "、" : ", ");
}

function translateEmbeddedTerms(value, language) {
  if (normalizeLanguage(language) !== "zh") return value;
  if (Array.isArray(value)) return value.map((entry) => translateEmbeddedTerms(entry, language));
  if (value && typeof value === "object") {
    return Object.fromEntries(
      Object.entries(value).map(([key, entry]) => [key, translateEmbeddedTerms(entry, language)])
    );
  }
  if (typeof value !== "string") return value;

  const replacements = [
    ...Object.entries(names.zh.abilities),
    ...Object.entries(names.zh.items),
    ...Object.entries(names.zh.areas),
    ...Object.entries(names.zh.elements)
  ].sort(([left], [right]) => right.length - left.length);

  return replacements.reduce(
    (translated, [english, chinese]) => translated.replaceAll(english, chinese),
    value
  );
}

export function challengeTargetLabel(challengeId, target, language = "en") {
  return content("challenges", challengeId, "targets", {}, language)?.[target.id] ?? target.label;
}

export function resourceFact(name, englishFact, language = "en") {
  return normalizeLanguage(language) === "zh"
    ? zhContent.resourceFacts[name] ?? englishFact
    : englishFact;
}
